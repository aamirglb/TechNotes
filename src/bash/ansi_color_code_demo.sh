#!/usr/bin/env bash

# Ansi Color Code Demo
BLACK='\033[0;30m'
RED='\033[0;31m'
GREEN='\033[0;32m'
BROWN='\033[0;33m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
CYAN='\033[0;36m'
LIGHT_GRAY='\033[0;37m'
DARK_GRAY='\033[1;30m'
LIGHT_RED='\033[1;31m'
LIGHT_GREEN='\033[1;32m'
YELLOW='\033[1;33m'
LIGHT_BLUE='\033[1;34m'
LIGHT_PURPLE='\033[1;35m'
LIGHT_CYAN='\033[1;36m'
WHITE='\033[1;37m'
NOCOLOR='\033[0m'

echo -e "${BLACK}I'm black color.${NOCOLOR}"
echo -e "${RED}I'm red color.${NOCOLOR}"
echo -e "${GREEN}I'm GREEN color.${NOCOLOR}"
echo -e "${BROWN}I'm BROWN color.${NOCOLOR}"
echo -e "${BLUE}I'm BLUE color.${NOCOLOR}"
echo -e "${PURPLE}I'm PURPLE color.${NOCOLOR}"
echo -e "${CYAN}I'm CYAN color.${NOCOLOR}"
echo -e "${LIGHT_GRAY}I'm LIGHT_GRAY color.${NOCOLOR}"
echo -e "${DARK_GRAY}I'm DARK_GRAY color.${NOCOLOR}"
echo -e "${LIGHT_RED}I'm LIGHT_RED color.${NOCOLOR}"
echo -e "${LIGHT_GREEN}I'm LIGHT_GREEN color.${NOCOLOR}"
echo -e "${YELLOW}I'm YELLOW color.${NOCOLOR}"
echo -e "${LIGHT_BLUE}I'm LIGHT_BLUE color.${NOCOLOR}"
echo -e "${LIGHT_PURPLE}I'm LIGHT_PURPLE color.${NOCOLOR}"
echo -e "${LIGHT_CYAN}I'm LIGHT_CYAN color.${NOCOLOR}"
echo -e "${WHITE}I'm WHITE color.${NOCOLOR}"
